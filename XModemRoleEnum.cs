namespace XModemProtocol {
    /// <summary>
    /// Denotes what role the XModemCommunicator instance is currently playing, or last played.
    /// </summary>
    public enum XModemRole {
        /// <summary>
        /// Represents the Receiver role. Default Role.
        /// </summary>
        Receiver,
